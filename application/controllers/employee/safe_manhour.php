	function get_manhour(){
		$employee_id = $this->input->post('employee_id');
		$date_incident = date('Y-m-d',strtotime($this->input->post('date_incident')));
		$date_from = date('Y-01-01',strtotime($date_incident));

		$qry = "SELECT SUM((hours_worked + (overtime / 60 / 60) - (lates / 60 / 60))) AS total_manhour 
				FROM {$this->db->dbprefix}employee_dtr as ed
				WHERE ed.date BETWEEN '{$date_from}' AND '{$date_incident}'
				AND employee_id = {$employee_id}";
		$result = $this->db->query($qry);

		$total_manhour = 0;
		if ($result && $result->num_rows() > 0){
			$total_manhour = $result->row()->total_manhour;
		}

		$response->total_manhour = number_format($total_manhour,2);
		$this->load->view('template/ajax',array('json' => $response));
	}
