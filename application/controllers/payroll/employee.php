	function get_custom_standard_transaction(){
		if( !IS_AJAX ){
			$this->session->set_flashdata('flashdata', 'Operation does not allow direct access.<br/>Please contact the System Administrator.');
			redirect(base_url().$this->module_link);
		}
		
		$response->msg = "";
		
		if( $this->user_access[$this->module_id]['edit'] == 1 ){
			$employee_id = $this->input->post('employee_id');
			$qry = "SELECT a.*, b.payroll_transaction_mode, c.transaction_label
			FROM {$this->db->dbprefix}payroll_standard_transaction_custom a
			LEFT JOIN {$this->db->dbprefix}payroll_transaction_mode b ON b.payroll_transaction_mode_id = a.transaction_mode_id
			LEFT JOIN {$this->db->dbprefix}payroll_transaction c ON c.transaction_code = a.transaction_code
			WHERE a.deleted = 0 AND c.deleted = 0 AND a.employee_id = {$employee_id}";
			$csts = $this->db->query( $qry );
			if( $csts->num_rows() > 0 ){
				$data['csts'] = $csts->result_array();
				$response->cst = $this->load->view( $this->userinfo['rtheme'] . '/' . $this->module_link . '/edit-cst_table', $data, true );
			}
			else{
				$response->cst = "";
			}
		}
		else{
			$response->msg = "You dont have sufficient privilege to execute this action! Please contact the System Administrator.";
			$response->msg_type = "error";
		}
		
		$data['json'] = $response;
		$this->load->view($this->userinfo['rtheme'].'/template/ajax', $data);
	}

